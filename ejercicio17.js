// Ejercicio 17: Calcular el área de un triángulo dados su base 4 y altura 3, y redondear el resultado a 2 decimales.

let base = 4;
let altura = 3;
let area = base * altura / 2;
let redondeado = area.toFixed(2);

console.log("Base: ", base);
console.log("Altura: ", altura);
console.log("Area: ", area);
console.log("Area redondeada a 2 decimales: ", redondeado);